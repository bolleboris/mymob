function extractAndSend($test) {
   $explodedFunc = explode('.',$test);
   echo "\n Calling function: $test\n\n";

   $current = BMUCore::b();
   foreach($explodedFunc as $key => $value) {
	  $exploded = explode('(',$value);
	  $tempValue = $exploded[0];
	  switch($tempValue) {
		 case 'List':
			$current = $current->GetList();
			break;
		 default:
			$current = $current->$tempValue();
			break;
	  }	  
   }
   echo "Request:\n\n";
   echo BMUCore::b()->buildInvocation();
   echo "\n\nResponse:\n\n";
   $output = BMUCore::b()->sendRequest();
   echo $output."\n";
   return $output;
}
